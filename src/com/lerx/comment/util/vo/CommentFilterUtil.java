package com.lerx.comment.util.vo;

import com.lerx.comment.vo.Comment;

public class CommentFilterUtil {

	private Comment c;
	private boolean found;
	private boolean rep;
	private boolean con;
	
	public Comment getC() {
		return c;
	}
	public void setC(Comment c) {
		this.c = c;
	}
	public boolean isFound() {
		return found;
	}
	public void setFound(boolean found) {
		this.found = found;
	}
	public boolean isRep() {
		return rep;
	}
	public void setRep(boolean rep) {
		this.rep = rep;
	}
	public boolean isCon() {
		return con;
	}
	public void setCon(boolean con) {
		this.con = con;
	}
	
	
}
