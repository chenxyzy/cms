package com.lerx.qa.util.vo;

import com.lerx.qa.vo.QaItem;


public class QaItemFilterUtil {

	private QaItem qi;
	private boolean found;
	private boolean rep;
	private boolean con;
	
	public QaItem getQi() {
		return qi;
	}
	public void setQi(QaItem qi) {
		this.qi = qi;
	}
	public boolean isFound() {
		return found;
	}
	public void setFound(boolean found) {
		this.found = found;
	}
	public boolean isRep() {
		return rep;
	}
	public void setRep(boolean rep) {
		this.rep = rep;
	}
	public boolean isCon() {
		return con;
	}
	public void setCon(boolean con) {
		this.con = con;
	}
	
	
	
}
