package com.lerx.bbs.vo;

public class ScoreScheme {
	private Integer id;
	private String schemeName;
	private String author;
	private String description;
	private boolean state;
	private int valueOfNewTheme;
	private int valueOfReply;
	private int valueOfSoul;
	private int valueOfUpload;
	private int valueOfDownload;
	private int valueOfMin;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getSchemeName() {
		return schemeName;
	}
	public void setSchemeName(String schemeName) {
		this.schemeName = schemeName;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public boolean isState() {
		return state;
	}
	public void setState(boolean state) {
		this.state = state;
	}
	public int getValueOfNewTheme() {
		return valueOfNewTheme;
	}
	public void setValueOfNewTheme(int valueOfNewTheme) {
		this.valueOfNewTheme = valueOfNewTheme;
	}
	public int getValueOfReply() {
		return valueOfReply;
	}
	public void setValueOfReply(int valueOfReply) {
		this.valueOfReply = valueOfReply;
	}
	public int getValueOfSoul() {
		return valueOfSoul;
	}
	public void setValueOfSoul(int valueOfSoul) {
		this.valueOfSoul = valueOfSoul;
	}
	public int getValueOfUpload() {
		return valueOfUpload;
	}
	public void setValueOfUpload(int valueOfUpload) {
		this.valueOfUpload = valueOfUpload;
	}
	public int getValueOfDownload() {
		return valueOfDownload;
	}
	public void setValueOfDownload(int valueOfDownload) {
		this.valueOfDownload = valueOfDownload;
	}
	public int getValueOfMin() {
		return valueOfMin;
	}
	public void setValueOfMin(int valueOfMin) {
		this.valueOfMin = valueOfMin;
	}
	
}
