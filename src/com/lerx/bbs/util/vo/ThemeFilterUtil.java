package com.lerx.bbs.util.vo;

import com.lerx.bbs.vo.BbsTheme;


public class ThemeFilterUtil {

	private BbsTheme t;
	private boolean found;
	private boolean rep;
	private boolean con;
	
	public BbsTheme getT() {
		return t;
	}
	public void setT(BbsTheme t) {
		this.t = t;
	}
	public boolean isFound() {
		return found;
	}
	public void setFound(boolean found) {
		this.found = found;
	}
	public boolean isRep() {
		return rep;
	}
	public void setRep(boolean rep) {
		this.rep = rep;
	}
	public boolean isCon() {
		return con;
	}
	public void setCon(boolean con) {
		this.con = con;
	}
	
	
}
