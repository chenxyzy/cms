package com.lerx.code.vo;

public class CodeCategory {
	private int id;
	private String name;
	private int currentCodeId;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCurrentCodeId() {
		return currentCodeId;
	}
	public void setCurrentCodeId(int currentCodeId) {
		this.currentCodeId = currentCodeId;
	}
	
	
}
