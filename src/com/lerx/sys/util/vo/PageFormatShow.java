package com.lerx.sys.util.vo;

public class PageFormatShow {
	private String first;
	private String end;
	private String last;
	private String next;
	private String prefix;
	private String suffix;
	private String countPrefix;
	private String jumpPrefix;
	private String eyeCatchingCode;
	public String getFirst() {
		return first;
	}
	public void setFirst(String first) {
		this.first = first;
	}
	public String getEnd() {
		return end;
	}
	public void setEnd(String end) {
		this.end = end;
	}
	public String getLast() {
		return last;
	}
	public void setLast(String last) {
		this.last = last;
	}
	public String getNext() {
		return next;
	}
	public void setNext(String next) {
		this.next = next;
	}
	public String getPrefix() {
		return prefix;
	}
	public void setPrefix(String prefix) {
		this.prefix = prefix;
	}
	public String getSuffix() {
		return suffix;
	}
	public void setSuffix(String suffix) {
		this.suffix = suffix;
	}
	public String getCountPrefix() {
		return countPrefix;
	}
	public void setCountPrefix(String countPrefix) {
		this.countPrefix = countPrefix;
	}
	public String getJumpPrefix() {
		return jumpPrefix;
	}
	public void setJumpPrefix(String jumpPrefix) {
		this.jumpPrefix = jumpPrefix;
	}
	public String getEyeCatchingCode() {
		return eyeCatchingCode;
	}
	public void setEyeCatchingCode(String eyeCatchingCode) {
		this.eyeCatchingCode = eyeCatchingCode;
	}
	
}
