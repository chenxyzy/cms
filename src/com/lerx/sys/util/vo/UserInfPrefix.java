package com.lerx.sys.util.vo;

public class UserInfPrefix {
	
	private String usernamePre;
	private String passwordPre;
	private String remNamePre;
	private String uidPre;
	private String openIDPre;
	
	public String getUsernamePre() {
		return usernamePre;
	}
	public void setUsernamePre(String usernamePre) {
		this.usernamePre = usernamePre;
	}
	
	public String getRemNamePre() {
		return remNamePre;
	}
	public void setRemNamePre(String remNamePre) {
		this.remNamePre = remNamePre;
	}
	public String getPasswordPre() {
		return passwordPre;
	}
	public void setPasswordPre(String passwordPre) {
		this.passwordPre = passwordPre;
	}
	public String getUidPre() {
		return uidPre;
	}
	public void setUidPre(String uidPre) {
		this.uidPre = uidPre;
	}
	public String getOpenIDPre() {
		return openIDPre;
	}
	public void setOpenIDPre(String openIDPre) {
		this.openIDPre = openIDPre;
	}
	
}
